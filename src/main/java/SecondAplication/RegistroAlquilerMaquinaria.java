package SecondAplication;

import java.math.BigDecimal;
import java.time.LocalDate;

public class RegistroAlquilerMaquinaria {
    private int id;
    private String maquinaria;
    private LocalDate fechaInicio;
    private LocalDate fechaTermino;
    private String operador;
    private BigDecimal horasTrabajo;
    private String cliente;
    private String estado;
    private String dni;
    private double total;
    private String usuario;

    // Constructor
    public RegistroAlquilerMaquinaria() {
    }

    // Getters y Setters
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getMaquinaria() {
        return maquinaria;
    }

    public void setMaquinaria(String maquinaria) {
        this.maquinaria = maquinaria;
    }

    public LocalDate getFechaInicio() {
        return fechaInicio;
    }

    public void setFechaInicio(LocalDate fechaInicio) {
        this.fechaInicio = fechaInicio;
    }

    public LocalDate getFechaTermino() {
        return fechaTermino;
    }

    public void setFechaTermino(LocalDate fechaTermino) {
        this.fechaTermino = fechaTermino;
    }

    public String getOperador() {
        return operador;
    }

    public void setOperador(String operador) {
        this.operador = operador;
    }

    public BigDecimal getHorasTrabajo() {
        return horasTrabajo;
    }

    public void setHorasTrabajo(BigDecimal horasTrabajo) {
        this.horasTrabajo = horasTrabajo;
    }

    public String getCliente() {
        return cliente;
    }

    public void setCliente(String cliente) {
        this.cliente = cliente;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }

    public String getDni() {
        return dni;
    }

    public void setDni(String dni) {
        this.dni = dni;
    }

    public double getTotal() {
        return total;
    }

    public void setTotal(double total) {
        this.total = total;
    }

    public String getUsuario() {
        return usuario;
    }

    public void setUsuario(String usuario) {
        this.usuario = usuario;
    }
}
